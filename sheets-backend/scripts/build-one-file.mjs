#!/usr/bin/env node
/**
 * Build ONE_FILE.gs from modular .gs sources (manual paste fallback).
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

const order = [
  'core/Response.gs',
  'core/Auth.gs',
  'core/SheetStore.gs',
  'catalog/CatalogRepository.gs',
  'catalog/CatalogService.gs',
  'parts/PartsRepository.gs',
  'parts/PartsService.gs',
  'parts/Photos.gs',
  'tech_process/TechProcessRepository.gs',
  'tech_process/TechProcessRules.gs',
  'tech_process/TechProcessService.gs',
  'assembly/AssemblyService.gs',
  'setup/PrepareSpreadsheet.gs',
  'AUTHORIZE_DRIVE.gs',
  'Code.gs',
];

const header = `/**
 * CNC Tech Process — весь backend в ОДНОМ файле для Apps Script (fallback).
 * AUTO-GENERATED by scripts/build-one-file.mjs — do not edit by hand.
 * Prefer clasp push of modular files (see SETUP.md).
 */

`;

const parts = [header];
for (const rel of order) {
  const text = readFileSync(join(root, rel), 'utf8').trim();
  parts.push(`// ===== ${rel} =====\n\n${text}\n\n`);
}
writeFileSync(join(root, 'ONE_FILE.gs'), parts.join(''));
console.log('built ONE_FILE.gs from', order.length, 'modules');
